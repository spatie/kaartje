const oceanSphereVertex = /* glsl */ `
  varying vec2 vUv;
  varying vec3 vViewPos;
  varying vec3 vNormal;

  void main() {
    vUv = uv;
    vNormal = normalize(normalMatrix * normal);
    vViewPos = (modelViewMatrix * vec4(position, 1.0)).xyz;
    gl_Position = projectionMatrix * modelViewMatrix * vec4(position, 1.0);
  }
`;

const oceanSphereFragment = /* glsl */ `
  precision highp float;
  uniform sampler2D uMask;
  uniform vec3 uWaterColor;
  uniform vec3 uLandColor;
  uniform float uWaterOpacity;
  uniform float uLandOpacity;
  varying vec2 vUv;
  varying vec3 vViewPos;
  varying vec3 vNormal;

  void main() {
    // Flip V (mask top-to-bottom, UVs bottom-to-top), mirror U, shift 0.5 to align with dot longitudes
    float land = texture2D(uMask, vec2(vUv.x, 1.0 - vUv.y)).r;

    // Water = blue, land = dark
    vec3 color = mix(uWaterColor, uLandColor, land);
    float opacity = mix(uWaterOpacity, uLandOpacity, land);

    // Fade backside
    vec3 viewDir = normalize(-vViewPos);
    float facing = dot(vNormal, viewDir);
    if (facing < 0.0) {
      opacity *= max(0.0, 1.0 + facing * 1.5);
    }

    gl_FragColor = vec4(color, opacity);
  }
`;

const dotVertex = /* glsl */ `
  uniform float uPixelRatio;
  uniform float uSize;
  attribute float aScale;
  varying float vFacing;

  void main() {
    vec4 mvPosition = modelViewMatrix * vec4(position, 1.0);
    vec3 viewDir = normalize(-mvPosition.xyz);
    vFacing = dot(normalize((modelViewMatrix * vec4(normalize(position), 0.0)).xyz), viewDir);

    gl_PointSize = uSize * aScale * uPixelRatio * (150.0 / -mvPosition.z);
    gl_Position = projectionMatrix * mvPosition;
  }
`;

const dotFragment = /* glsl */ `
  precision highp float;
  uniform vec3 uColor;
  varying float vFacing;

  void main() {
    float d = length(gl_PointCoord - vec2(0.5));
    if (d > 0.4) discard;

    // Front = fully opaque, back = fade out
    float opacity = vFacing > 0.0
      ? 1.0
      : max(0.0, 1.0 + vFacing * 1.5);

    gl_FragColor = vec4(uColor, opacity);
  }
`;

