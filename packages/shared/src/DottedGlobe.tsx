export interface DottedGlobeProps {
  /** Spacing between land dots in degrees (default: 1.1) */
  dotStep?: number;
  /** Globe radius (default: 2.5) */
  radius?: number;
  /** Land dot color (default: '#ede6db') */
  dotColor?: string;
  /** Land dot size multiplier (default: 0.45) */
  dotSize?: number;
  /** Water/ocean color (default: '#4a7fb5') */
  waterColor?: string;
  /** Water opacity (default: 0.12) */
  waterOpacity?: number;
  /** Postcard arc destinations */
  arcs?: ArcTarget[];
  /** Delay in seconds before arcs start animating (default: 0) */
  arcDelay?: number;
  /** Auto-rotation speed in rad/s (default: 0.1) */
  rotationSpeed?: number;
  /** Live postcard cards to animate flying toward the globe */
  liveCards?: LiveCard[];
  /** Pre-existing postcards from the database — rendered as permanent stamps */
  persistentCards?: LiveCard[];
  /** Called on stamp hover (preview) */
  onCardHover?: (data: StampHoverData | null) => void;
  /** Called on stamp click (pin) */
  onCardSelect?: (data: StampHoverData | null) => void;
  /** Called when a live card finishes its flight animation */
  onCardLanded?: (card: LiveCard, clockTime: number) => void;
  /** Pause globe rotation (e.g. when a card is focused) */
  paused?: boolean;
}

