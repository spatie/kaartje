// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const DEG = Math.PI / 180;

// ---------------------------------------------------------------------------
// Decode land mask once at module level (~1ms)
// ---------------------------------------------------------------------------

const LAND_MASK: Uint8Array = (() => {
  const bin = atob(LAND_MASK_B64);
  const arr = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) arr[i] = bin.charCodeAt(i);
  return arr;
})();

function sampleMask(lat: number, lng: number): boolean {
  const x = Math.floor(((lng + 180) / 360) * MASK_W) % MASK_W;
  const y = Math.max(0, Math.min(MASK_H - 1, Math.floor(((90 - lat) / 180) * MASK_H)));
  const idx = y * MASK_W + x;
  return (LAND_MASK[idx >> 3] & (1 << (idx & 7))) !== 0;
}

// ---------------------------------------------------------------------------
// Precalculated dot data (generated by scripts/generate-land-dots.ts)
// Stored as Int16 lat/lng pairs, decoded to xyz + scale at load time
// ---------------------------------------------------------------------------

interface LandData {
  positions: Float32Array;
  scales: Float32Array;
}

function scaleForLat(lat: number): number {
  if (lat < -65) return Math.max(0.3, (lat + 80) / 15);
  if (lat > 75) return Math.max(0.3, (90 - lat) / 15);
  return 1;
}

function decodePrecalcDots(radius: number): LandData {
  const bin = atob(DOTS_B64);
  const bytes = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) bytes[i] = bin.charCodeAt(i);
  const packed = new Int16Array(bytes.buffer);

  const count = packed.length / 2;
  const positions = new Float32Array(count * 3);
  const scales = new Float32Array(count);

  for (let i = 0; i < count; i++) {
    const lat = packed[i * 2] / 100;
    const lng = packed[i * 2 + 1] / 100;
    const [x, y, z] = latLngToVec3(lat, lng, radius);
    positions[i * 3] = x;
    positions[i * 3 + 1] = y;
    positions[i * 3 + 2] = z;
    scales[i] = scaleForLat(lat);
  }

  return { positions, scales };
}

function computeLandDots(step: number, radius: number): LandData {
  // Use precalculated lat/lng data for default step
  if (step === DOT_STEP) return decodePrecalcDots(radius);

  // Fallback: compute at runtime for non-default step
  const positions: number[] = [];
  const scales: number[] = [];

  for (let lat = -80 + step / 2; lat < 90; lat += step) {
    const cosLat = Math.cos(lat * DEG);
    const lngStep = cosLat > 0.05 ? step / cosLat : 360;
    const scale = scaleForLat(lat);

    for (let lng = -180 + lngStep / 2; lng < 180; lng += lngStep) {
      if (sampleMask(lat, lng)) {
        const [x, y, z] = latLngToVec3(lat, lng, radius);
        positions.push(x, y, z);
        scales.push(scale);
      }
    }
  }

  return {
    positions: new Float32Array(positions),
    scales: new Float32Array(scales),
  };
}

// ---------------------------------------------------------------------------
// Land mask as a DataTexture (for the inner sphere shader)
// ---------------------------------------------------------------------------

const MASK_TEXTURE: THREE.DataTexture = (() => {
  const data = new Uint8Array(MASK_W * MASK_H * 4);
  for (let i = 0; i < MASK_W * MASK_H; i++) {
    const isLand = (LAND_MASK[i >> 3] & (1 << (i & 7))) !== 0;
    const v = isLand ? 255 : 0;
    data[i * 4] = v;
    data[i * 4 + 1] = v;
    data[i * 4 + 2] = v;
    data[i * 4 + 3] = 255;
  }
  const tex = new THREE.DataTexture(data, MASK_W, MASK_H, THREE.RGBAFormat);
  tex.needsUpdate = true;
  tex.minFilter = THREE.LinearFilter;
  tex.magFilter = THREE.LinearFilter;
  tex.wrapS = THREE.RepeatWrapping;
  return tex;
})();

// ---------------------------------------------------------------------------
// Shaders
// ---------------------------------------------------------------------------

const oceanSphereVertex = /* glsl */ `
  varying vec2 vUv;
  varying vec3 vViewPos;
  varying vec3 vNormal;

  void main() {
    vUv = uv;
    vNormal = normalize(normalMatrix * normal);
    vViewPos = (modelViewMatrix * vec4(position, 1.0)).xyz;
    gl_Position = projectionMatrix * modelViewMatrix * vec4(position, 1.0);
  }
`;

const oceanSphereFragment = /* glsl */ `
  precision highp float;
  uniform sampler2D uMask;
  uniform vec3 uWaterColor;
  uniform vec3 uLandColor;
  uniform float uWaterOpacity;
  uniform float uLandOpacity;
  varying vec2 vUv;
  varying vec3 vViewPos;
  varying vec3 vNormal;

  void main() {
    // Flip V (mask top-to-bottom, UVs bottom-to-top), mirror U, shift 0.5 to align with dot longitudes
    float land = texture2D(uMask, vec2(vUv.x, 1.0 - vUv.y)).r;

    // Water = blue, land = dark
    vec3 color = mix(uWaterColor, uLandColor, land);
    float opacity = mix(uWaterOpacity, uLandOpacity, land);

    // Fade backside
    vec3 viewDir = normalize(-vViewPos);
    float facing = dot(vNormal, viewDir);
    if (facing < 0.0) {
      opacity *= max(0.0, 1.0 + facing * 1.5);
    }

    gl_FragColor = vec4(color, opacity);
  }
`;

const dotVertex = /* glsl */ `
  uniform float uPixelRatio;
  uniform float uSize;
  attribute float aScale;
  varying float vFacing;

  void main() {
    vec4 mvPosition = modelViewMatrix * vec4(position, 1.0);
    vec3 viewDir = normalize(-mvPosition.xyz);
    vFacing = dot(normalize((modelViewMatrix * vec4(normalize(position), 0.0)).xyz), viewDir);

    gl_PointSize = uSize * aScale * uPixelRatio * (150.0 / -mvPosition.z);
    gl_Position = projectionMatrix * mvPosition;
  }
`;

const dotFragment = /* glsl */ `
  precision highp float;
  uniform vec3 uColor;
  varying float vFacing;

  void main() {
    float d = length(gl_PointCoord - vec2(0.5));
    if (d > 0.4) discard;

    // Front = fully opaque, back = fade out
    float opacity = vFacing > 0.0
      ? 1.0
      : max(0.0, 1.0 + vFacing * 1.5);

    gl_FragColor = vec4(uColor, opacity);
  }
`;

