const DotLayer = memo(function DotLayer({
  positions,
  scales,
  color,
  size,
}: {
  positions: Float32Array;
  scales: Float32Array;
  color: string;
  size: number;
}) {
  const { gl, size: viewportSize } = useThree();
  const materialRef = useRef<ShaderMaterial>(null!);

  // Scale dots to appear consistent across screen sizes
  // Use the smaller dimension so dots fit both narrow and short screens
  const responsiveSize = useMemo(() => {
    const refSize = 900; // reference: dots tuned for ~900px smallest dimension
    const smallest = Math.min(viewportSize.width, viewportSize.height);
    const scale = Math.max(0.35, Math.min(1.2, smallest / refSize));
    return size * scale;
  }, [size, viewportSize.width, viewportSize.height]);

  const geometry = useMemo(() => {
    const geo = new BufferGeometry();
    geo.setAttribute("position", new BufferAttribute(positions, 3));
    geo.setAttribute("aScale", new BufferAttribute(scales, 1));
    return geo;
  }, [positions, scales]);

  const uniforms = useMemo(() => ({
    uColor: { value: new Color(color) },
    uPixelRatio: { value: 1 },
    uSize: { value: size },
  }), [color, size]);

  // Update dynamic uniforms without recreating geometry or material
  useEffect(() => {
    if (materialRef.current) {
      materialRef.current.uniforms.uSize.value = responsiveSize;
      materialRef.current.uniforms.uPixelRatio.value = gl.getPixelRatio();
    }
  }, [responsiveSize, gl]);

  useEffect(() => {
    return () => geometry.dispose();
  }, [geometry]);

  return (
    <points geometry={geometry}>
      <shaderMaterial
        ref={materialRef}
        vertexShader={dotVertex}
        fragmentShader={dotFragment}
        uniforms={uniforms}
        transparent
      />
    </points>
  );
});

