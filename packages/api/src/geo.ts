export function coordsForCountry(country: string) {
  // Try exact match first, then case-insensitive
  if (COUNTRY_COORDS[country]) return COUNTRY_COORDS[country];

  const key = Object.keys(COUNTRY_COORDS).find((k) => k.toLowerCase() === country.toLowerCase());
  return key ? COUNTRY_COORDS[key] : null;
}
